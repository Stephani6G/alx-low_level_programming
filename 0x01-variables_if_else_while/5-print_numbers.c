#include <stdio.h>
/**
 * main - Prints all single digit numbers of base 10 starting from 0
 *
 * Description: using the main function
 * this program prints " Programming all single digits of base 10
 * Return: 0
 */
int main(void)
{
	int i;

	for (i = 0; i < 10; i++)

	{
		printf("%i", i);
	}

	printf("\n");

	return (0);
}
